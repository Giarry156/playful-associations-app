    <script>
        const gamesSliders = document.querySelectorAll('#games-slider');

        for (const gamesSlider of gamesSliders) {
            chevronsCheck(gamesSlider);

            gamesSlider.addEventListener('scroll', (e) => {
                chevronsCheck(gamesSlider);
            });
        }

        const chevrons = document.querySelectorAll('.games-section-chevron');

        function updateSliderByChevron(event) {
            const target = event.target.tagName.toLowerCase() !== 'div' ? event.target.closest("div") : event.target;
            const gamesSlider = target.parentElement.querySelector('#games-slider');
            const slide = target.dataset.direction === 'right' ? gamesSlider.clientWidth : -gamesSlider.clientWidth;

            gamesSlider.scrollBy({
                top: 0,
                left: slide,
                behavior: 'smooth'
            });
        }

        for (const chevron of chevrons) {
            chevron.addEventListener('click', updateSliderByChevron);
        }

        function chevronsCheck(gamesSlider) {
            const scrollValue = gamesSlider.scrollLeft;
            const maxScrollValue = gamesSlider.scrollWidth - gamesSlider.clientWidth;
            const leftChevron = gamesSlider.parentElement.querySelector('.games-section-chevron-left');
            const rightChevron = gamesSlider.parentElement.querySelector('.games-section-chevron-right');

            if (scrollValue <= 0) {
                leftChevron.classList.add("games-section-chevron-hidden");
                rightChevron.classList.remove("games-section-chevron-hidden");
            } else if (scrollValue > 0 && scrollValue < maxScrollValue) {
                leftChevron.classList.remove("games-section-chevron-hidden");
                rightChevron.classList.remove("games-section-chevron-hidden");
            } else {
                leftChevron.classList.remove("games-section-chevron-hidden");
                rightChevron.classList.add("games-section-chevron-hidden");
            }
        }
    </script>
